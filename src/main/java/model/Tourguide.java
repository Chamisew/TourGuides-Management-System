package model;

public class Tourguide {
	private String tougID;
	private String name;
	private String spokLang;
	private String avai;
	private String contact;
	
	public String getTougID() {
		return tougID;
	}
	public void setTougID(String tougID) {
		this.tougID = tougID;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getSpokLang() {
		return spokLang;
	}
	public void setSpokLang(String spokLang) {
		this.spokLang = spokLang;
	}
	public String getAvialability() {
		return avai;
	}
	public void setAvialability(String avai) {
		this.avai = avai;
	}
	public String getContact() {
		return contact;
	}
	public void setContact(String contact) {
		this.contact = contact;
	}

}
